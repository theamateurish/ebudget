    private void chkAllActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_chkAllActionPerformed
        // TODO add your handling code here:
        tblEarmark.getModel().removeTableModelListener(tblModelListen);
        javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel)tblEarmark.getModel();
        short count = 0;
        for (short abc = 0; abc < modelo.getRowCount(); abc++) {
            modelo.setValueAt(chkAll.isSelected(), abc, 0);
            count += (chkAll.isSelected() ? 1 : 0);
        }
        lblSelected.setText(count + " Selected Transactions");
        tblEarmark.getModel().addTableModelListener(tblModelListen);
    }//GEN-LAST:event_chkAllActionPerformed

    private void btnCancelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelActionPerformed
        // TODO add your handling code here:
        String[] options = {"Yes", "No"};
        boolean verify = javax.swing.JOptionPane.showOptionDialog(
                this,
                "Are you sure to CANCEL the selected Earmark?", title,
                javax.swing.JOptionPane.YES_NO_OPTION,
                javax.swing.JOptionPane.QUESTION_MESSAGE,
                null,
                options,
                options[options.length - 1]) == javax.swing.JOptionPane.NO_OPTION;
        if (verify) return;

        String sqlCmd =
                "UPDATE " +
                    "buds.earmark_main " +
                "SET " +
                    "status = 2 " +
                "WHERE " +
                    "(earmrk_id = ?)";
        try (budget.DBaseLink jdbc = new budget.DBaseLink();
                java.sql.PreparedStatement psmt = jdbc.prepareStatement(sqlCmd)) {
            javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel)tblEarmark.getModel();
            for (short abc = 0; abc < modelo.getRowCount(); abc++) {
                boolean test = Boolean.parseBoolean(modelo.getValueAt(abc, 0).toString());
                if (!test) continue;
                
                String value = modelo.getValueAt(abc, 1).toString();
                psmt.setString(1, value);
                psmt.addBatch();
            }
            psmt.executeBatch();

        } catch (java.sql.SQLException ex) {
            javax.swing.JOptionPane.showMessageDialog(this, ex.getMessage(), title, javax.swing.JOptionPane.ERROR_MESSAGE);
            javax.swing.JOptionPane.showMessageDialog(this, ex.getNextException(), title, javax.swing.JOptionPane.ERROR_MESSAGE);
            java.util.logging.Logger.getLogger(VoidForm.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        } finally {

        }
    }//GEN-LAST:event_btnCancelActionPerformed

