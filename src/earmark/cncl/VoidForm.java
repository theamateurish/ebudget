    private final javax.swing.event.TableModelListener tblModelListen = new javax.swing.event.TableModelListener() {
        @Override
        public void tableChanged(javax.swing.event.TableModelEvent evt) {
            if (evt.getType() != javax.swing.event.TableModelEvent.UPDATE) return;

            javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel) tblEarmark.getModel();
            short count = 0;
            for (short abc = 0; abc < modelo.getRowCount(); abc++) {
                boolean checked = Boolean.parseBoolean(modelo.getValueAt(abc, 0).toString());
                count += (checked ? 1 : 0);
            }
            lblSelected.setText(count + " Selected Transactions");
            chkAll.setSelected(modelo.getRowCount() == count);
        }
    };
