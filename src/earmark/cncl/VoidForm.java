    private void reQueryForCancel() {
        String sqlCmd =
                "SELECT " +
                    "earmrk_id, " +
                    "transact, " +
                    "total_amnt, " +
                    "particular " +
                "FROM " +
                    "buds.earmark_main " +
                "WHERE " +
                    (txtSearch.getText().trim().isEmpty() ? "" : "(earmrk_id LIKE ?) AND ") +
                    "(status = 0) " +
                "ORDER BY " +
                    "earmrk_id";
        try (budget.DBaseLink jdbc = new budget.DBaseLink();
                java.sql.PreparedStatement psmt = jdbc.prepareStatement(sqlCmd)) {
            if (!txtSearch.getText().trim().isEmpty()) psmt.setString(1, "%" + txtSearch.getText() + "%");
            try (java.sql.ResultSet rst = psmt.executeQuery()) {
                javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel)tblEarmark.getModel();
                modelo.setRowCount(0);
                while (rst.next()) {
                    Object[] rowData = {
                        false,
                        rst.getString(1),
                        new java.text.SimpleDateFormat("MM/dd/yyyy").format(rst.getDate(2)),
                        rst.getDouble(3),
                        rst.getString(4)
                    };
                    modelo.addRow(rowData);
                }
            }

        } catch (java.sql.SQLException ex) {
            javax.swing.JOptionPane.showMessageDialog(this, ex.getMessage(), title, javax.swing.JOptionPane.ERROR_MESSAGE);
            java.util.logging.Logger.getLogger(VoidForm.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        } finally {

        }
    }
    
